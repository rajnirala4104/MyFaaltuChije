# types of matrix
general_matrix = [[1, 2, 3], [4, 5, 6], [7, 8, 9]] # General matrix
square_matrix = [[1, 2, 3], [4, 5, 6], [7, 8, 9]] # Square matrix - are equal if the number of rows and columns are equal 
diagonal_matrix = [[1, 0, 0, 0], [0, 2, 0, 0], [0, 0, 3, 0], [0, 0, 0, 4]] # Diagonal matrix - are equal if the diagonal elements are equal
symetric_matrix = [[1, 2, 3], [2, 4, 5], [3, 5, 6]] #Symmetric matrix - are equal if the transpose of the matrix is equal to itself
identity_matrix = [[1, 0, 0], [0, 1, 0], [0, 0, 1]] # Identity matrix - 3x3 
scaler_matrix = [[2, 0, 0], [0, 2, 0], [0, 0, 2]] # scaler matrix are equal if the product of the matrix and the scaler is equal
zero_matrix = [[0, 0, 0], [0, 0, 0], [0, 0, 0]] # zero matrix has all the elements equal to zero
coulmn_matrix = [[1], [2],[3],[4]] # 4x1 column matrix has one column and multiple rows
row_matrix = [[1,2,3,4]] # 1x4 row matrix has one row and multiple columns
