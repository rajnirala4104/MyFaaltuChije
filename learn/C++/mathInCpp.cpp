#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << max(23, 10) << endl;  // max(a, b) -> to find a largest number from two numbers
    cout << min(100, 40) << endl; // min(a,b) -> to find a smallest number from two numbers
    cout << sqrt(64) << endl;     // to find a square root of a number
    cout << round(2.5);           // to find round figure of a number e.g -> 2.4 or less then thisz would be 2 and 2.5 or more then this would be 3

    return 0;
}